package FirstLesson;
/*
Soru1: Integer ve Long veri tiplerinin max and min değerlerini bulup console yazdıralım
 */
public class Question01 {

    public static void main(String[] args) {

        int max_int = Integer.MAX_VALUE;
        int min_int = Integer.MIN_VALUE;

        System.out.println("Integerin max degeri = " + max_int);
        System.out.println("Integerin min degeri = " + min_int);

        long max_long = Long.MAX_VALUE;
        Long min_long = Long.MIN_VALUE;

        System.out.println("Long max degeri = " + max_long);
        System.out.println("Long min degeri = " + min_long);


    }
}
